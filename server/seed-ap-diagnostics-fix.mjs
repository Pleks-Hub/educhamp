/**
 * Seed script: Replace all AP/SAT diagnostic questions with real, AP-aligned questions.
 * Choices format: [{ label: 'A', text: '...' }, { label: 'B', text: '...' }, ...]
 * correctAnswer: 'A' | 'B' | 'C' | 'D'
 * Run: node server/seed-ap-diagnostics-fix.mjs
 */
import mysql from '/home/ubuntu/educhamp/node_modules/.pnpm/mysql2@3.15.1/node_modules/mysql2/promise.js';

const conn = await mysql.createConnection(process.env.DATABASE_URL);

// Look up course id by courseCode
async function getCourseId(courseCode) {
  const [rows] = await conn.execute('SELECT id FROM courses WHERE courseCode = ?', [courseCode]);
  if (!rows.length) throw new Error(`Course not found: ${courseCode}`);
  return rows[0].id;
}

// Convert plain string choices to { label, text } format
function makeChoices(arr) {
  const labels = ['A', 'B', 'C', 'D'];
  return arr.map((text, i) => ({ label: labels[i], text }));
}

// Replace all diagnostic questions for a course
async function replaceQuestionsForCourse(courseId, questions) {
  await conn.execute('DELETE FROM diagnosticQuestions WHERE courseId = ?', [courseId]);
  for (let i = 0; i < questions.length; i++) {
    const q = questions[i];
    await conn.execute(
      `INSERT INTO diagnosticQuestions
        (questionId, courseId, questionText, questionType, choices, correctAnswer, mapsToUnit, mapsToSkills, difficulty, explanation, sortOrder)
       VALUES (?, ?, ?, 'multiple_choice', ?, ?, ?, ?, ?, ?, ?)`,
      [
        q.questionId,
        courseId,
        q.questionText,
        JSON.stringify(makeChoices(q.choices)),
        q.correctAnswer, // label: 'A' | 'B' | 'C' | 'D'
        q.mapsToUnit,
        JSON.stringify(q.mapsToSkills),
        q.difficulty,
        q.explanation,
        i + 1,
      ]
    );
  }
  console.log(`✓ Replaced ${questions.length} questions for courseId ${courseId} (${questions[0]?.questionId?.split('-')[0]})`);
}

